
//data type in swift language

//1. Int
var number = 10

//2. Float
var floatNumber = 3.14

//3. Double
var doubleNumber = 3.14159265359

//4. String
var string = "Hello, World!"

//5. Bool
var boolean = true

//result of the data type
print("The number is \(number)")
print("The float number is \(floatNumber)")
print("The double number is \(doubleNumber)")
print("The string is \(string)")
print("The boolean is \(boolean)")

